    private void cargarDatos(){
        switch(tab){
          case 0:
            Stok = s.getStock();//Obtenemos los datos y almacenamos aca
            s.SearchCliente(Table_Clientes1, "", num_registro,pageSize);
            maxReg = Stok.size();//Obtenemos la cantidad de registros 
            EliminarSC();
            numcliente = c.getClientes();//Obtenemos los datos y almacenamos aca
            c.SearchCliente(Table_ReportesCLT1, "", 0, pageSize);
            break;
          case 1:
            numcliente = c.getClientes();//Obtenemos los datos y almacenamos aca
            c.SearchCliente(Table_Clientes, "", num_registro, pageSize);
            maxReg = numcliente.size();//Obtenemos la cantidad de registros  
            break;
          case 2:
            Stok = s.getStock();//Obtenemos los datos y almacenamos aca
            s.SearchCliente(Table_BodegaPDT, "", num_registro, pageSize);
            maxReg = Stok.size();//Obtenemos la cantidad de registros 
            break;
              case 3:                  
            vent = v.getVentas();//Obtenemos los datos y almacenamos aca
            v.SearchVentas(Table_Dpt, "", num_registro, pageSize,null,null);
            maxReg = vent.size();//Obtenemos la cantidad de registros 
            break;
              
    }
    pageCount = (maxReg / pageSize);
    //Ajusta el numero de pagina si la √∫ltima pagina contiene una parte
    if(maxReg % pageSize >0){
        //Saber si la ultima pagina que visualizamos contiene mas de 6 registros
        //y vemos si asignamos pagina
        pageCount =pageCount+1;
    }
    
    Label_Paginas.setText("pagina" + "1"+ "/"+String.valueOf(pageCount));
    Label_Paginas1.setText("pagina" + "1"+ "/"+String.valueOf(pageCount));
    Label_Paginas3.setText("pagina" + "1"+ "/"+String.valueOf(pageCount));
    Label_Paginas4.setText("pagina" + "1"+ "/"+String.valueOf(pageCount));
}
